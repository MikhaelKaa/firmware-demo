/**
 * @file stubs.h
 * @brief Стубы платформозависимых функций для хост-тестов.
 *
 * Позволяет компиляцию модулей firmware-core на x86_64 без зависимости
 * от CMSIS, регистров STM32 и других embedded-специфичных заголовков.
 */

#ifndef TEST_STUBS_H
#define TEST_STUBS_H

#include <stdint.h>

/* ------------------------------------------------------------------ */
/* Инициализация стубов                                                */
/* ------------------------------------------------------------------ */

/**
 * @brief Инициализировать все моки значениями по умолчанию.
 *
 * Сбрасывает счётчик циклов к нулю, устанавливает system_core_clock = 168 МГц.
 * Вызывать один раз перед запуском тестов (или в main()).
 */
void stubs_init(void);

/* ------------------------------------------------------------------ */
/* Управление моком CYCCNT                                             */
/* ------------------------------------------------------------------ */

/**
 * @brief Сбросить мок-счётчик циклов к нулю.
 */
void stubs_reset_cycles(void);

/**
 * @brief Продвинуть мок-счётчик на указанное количество циклов.
 */
void stubs_advance_cycles(uint32_t cycles);

/**
 * @brief Продвинуть мок-счётчик на указанное количество микросекунд
 *         (конвертация через system_core_clock).
 */
void stubs_advance_time_us(uint32_t us);

/* ------------------------------------------------------------------ */
/* hardware_panic() стуб                                               */
/* ------------------------------------------------------------------ */

/**
 * @brief Стуб для аппаратного паник-обработчика.
 *
 * Вызывается при фатальных ошибках вместо реального сброса МК.
 */
void hardware_panic(const char *file, int line, const char *reason);

#endif /* TEST_STUBS_H */